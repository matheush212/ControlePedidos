//-------------------------------------------------------------------------------Dependencias de codigo-----------------------------------------------------------------------------//
var express = require('express');
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//


//--------------------------------------------------------------------------Requires de Funcoes Externas----------------------------------------------------------------------------//
var usings = require('./public/js/ServerSide_JS/Usings.js');
var routes = require('./views/routes/Routes.js');
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//

var app = express(); // API que executa e inicia o server express
var port = 80;
usings.Usings(express, __dirname, app); // FunCAOo responsAvel por carregar todos os mOdulos usados e permissoes de acesso ao server
app.listen(port); // Servidor comeca a escutar na porta especificada


//----------------------------------------------------------------------------------ROTAS - GET-------------------------------------------------------------------------------------//
routes.GetRoute(__dirname, app); // Funcao responsavel por executar o metodo GET
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//


//--------------------------------------------------------------------------------ROTAS - POST--------------------------------------------------------------------------------------//
routes.PostRoute(__dirname, app); // Funcao responsavel por executar o metodo POST
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//